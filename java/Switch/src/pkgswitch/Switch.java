package pkgswitch;


public class Switch {

    public static void main(String[] args) {
        int number = 10;

        switch(number) {

            case 1:
            System.out.println("você digitou 1");
            break;

            case 2:
            System.out.println("você digitou 2");
            break;

            case 3:
            System.out.println("você digitou 3");
            break;

            default:
            System.out.println("número inválido");
        }
    }
    
}
